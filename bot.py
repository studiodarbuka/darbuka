def get_schedule_start():
    today = datetime.datetime.now(JST)
    # ã€Œä»Šé€±ã®æ—¥æ›œã€ã‚’è¦‹ã¤ã‘ã‚‹ï¼ˆä»Šæ—¥ã‚’å«ã‚ãšï¼‰
    days_since_sunday = (today.weekday() + 1) % 7  # æœˆ=0 â†’ æ—¥=6
    this_sunday = today - datetime.timedelta(days=days_since_sunday)
    # ä»Šé€±ã‚’1é€±ç›®ã¨ã—ã¦ã€3é€±é–“å¾Œã®æ—¥æ›œã‚’å–å¾—
    target = this_sunday + datetime.timedelta(weeks=3)
    return target.replace(hour=0, minute=0, second=0, microsecond=0)

def generate_week_schedule():
    start = get_schedule_start()
    return [(start + datetime.timedelta(days=i)).strftime("%Y-%m-%d (%a)") for i in range(7)]

